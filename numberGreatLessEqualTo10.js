const amount = 15;

console.log(
  "Program to find whether the Number Greater/Lesser/Equal to 10 !!!"
);

if (amount < 10) {
  console.log(`Given number ${amount} is less than 10`);
} else if (amount == 10) {
  console.log(`Given number ${amount} is equal to 10`);
} else {
  console.log(`Given number ${amount} is greater than 10`);
}
